// 계약 완료 핸들러
export const completeContractHandler = (
  contractId: string,
): CompleteContractResponse => {
  const mockData = mockContractDetailList[contractId];

  const response: CompleteContractResponse = {
    contractId: mockData.contractId,
    crId: mockData.crId,
    crName: mockData.crName,
    drId: mockData.drId,
    drName: mockData.drName,
    la: mockData.la,
    ir: mockData.ir,
    contractDt: mockData.contractDt,
    matDt: mockData.matDt,
    lt: mockData.lt,
    earlypayFee: mockData.earlypayFee,
    repayType: mockData.repayType,
    repayTypeName: mockData.repayTypeName,
    mpDt: mockData.mpDt,
    dir: mockData.dir,
    defCnt: mockData.defCnt,
    earlypay: mockData.earlypay,
    pnTransFlag: mockData.pnTransFlag,
    addTerms: mockData.addTerms || "",
    contractStatus: "CONTRACTED", // 승인 시 상태는 CONTRACTED로 변경
    contractStatusName: "완료",
    message: mockData.message || "",
    rejectMessage: "", // 승인이므로 거절 메시지는 빈 문자열
    rejectedAt: "", // 승인이므로 거절 시간은 빈 문자열
    createdAt: mockData.createdAt,
    updatedAt: new Date().toISOString().split("T")[0].replace(/-/g, "."), // 현재 시간으로 업데이트
  };

  return response;
};
